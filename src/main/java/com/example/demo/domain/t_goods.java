package com.example.demo.domain;

public class t_goods {       //商品上传基本信息
    private String g_id;
    private String g_name;
    private String g_brand;
    private String g_intro;
    private String g_Area;
    private double g_price;
    private String g_date;
    private String g_place;
    private double g_weight;

    public String getG_id() { return g_id; }

    public void setG_id(String g_id) { this.g_id = g_id; }

    public String getG_name() { return g_name; }

    public void setG_name(String g_name) { this.g_name = g_name; }

    public String getG_brand() { return g_brand; }

    public void setG_brand(String g_brand) { this.g_brand = g_brand; }

    public String getG_intro() { return g_intro; }

    public void setG_intro(String g_intro) { this.g_intro = g_intro; }

    public String getG_Area() { return g_Area;}

    public void setG_Area(String g_Area) { this.g_Area = g_Area; }

    public double getG_price() { return g_price;}

    public void setG_price(double g_price) { this.g_price = g_price;}

    public String getG_date() { return g_date; }

    public void setG_date(String g_date) { this.g_date = g_date; }

    public String getG_place() { return g_place; }

    public void setG_place(String g_place) { this.g_place = g_place;}

    public double getG_weight() { return g_weight;}

    public void setG_weight(double g_weight) { this.g_weight = g_weight; }
}
